package de.gishmo.mvp4g.example.multipresenter.shared.dto;


public class MultiPresenterModel {

  private String id;
  private String created;

  public MultiPresenterModel() {
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getCreated() {
    return created;
  }

  public void setCreated(String created) {
    this.created = created;
  }
}
